package Day_1;
import java.util.Scanner;
public class sumoftwonumber {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter the Number");
        int n=sc.nextInt();
        int no1=sc.nextInt();
        int sum=0;
        sum=n+no1;
        System.out.println("Sum of number is:"+sum);
    }
}